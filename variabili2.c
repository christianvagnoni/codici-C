#include <stdio.h>
/*
* Addizzione es: c = a + b
* Sottotrazione es: c = a - b
* Moltiplicazione es c = a * b
* Divisione es: c = a / b
* Modulo es: c = 11 % 2 (c = 1 di resto)
* Incremento a++ è un esemplificazione di a = a + 1
* Decremento a-- è un esemplificazione di a = a - 1
*/


int main(){
    int a = 0;
    a++;
    printf("Valore di a = %d\n" , a);
    a++;
    printf("Valore di a = %d\n" , a);
}